package com.example.fyproject;

public class PDHandler {

    public String Name, Age, Gender,Weight, PhoneNumber, Consultant, Next_of_kin, Number;

    public PDHandler(){

    }

    public PDHandler(String Name, String Age, String Gender,String Weight, String PhoneNumber, String Consultant, String Next_of_kin, String Number){
        this.Name = Name;
        this.Age = Age;
        this.Gender = Gender;
        this.Weight = Weight;
        this.PhoneNumber = PhoneNumber;
        this.Consultant = Consultant;
        this.Next_of_kin = Next_of_kin;
        this.Number = Number;
    }

}
